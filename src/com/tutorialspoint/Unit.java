package com.tutorialspoint;

public class Unit {
	private int unitId;  
	private String name;  
	private int exp;  
	private int bblvl;
	private String type;
	  
	public int getId() {  
	    return unitId;  
	}  
	public void setId(int id) {  
	    this.unitId = id;  
	}  
	public String getName() {  
	    return name;  
	}  
	public void setName(String name) {  
	    this.name = name;  
	}  
	public int getExp() {  
	    return exp;  
	}  
	public void setExp(int exp) {  
	    this.exp = exp;  
	}  
	public int getbblvl() {  
	    return bblvl;  
	}  
	public void setbblvl(int bblvl) {  
	    this.bblvl = bblvl;  
	}  
	public String gettype() {  
	    return type;  
	}  
	public void settype(String type) {  
	    this.type = type;  
	}  
}
